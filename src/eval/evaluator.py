from typing import List, Dict, Any, Optional
import pandas as pd
from ragas import evaluate
from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall
from datasets import Dataset
from ..utils.decorators import time_execution

class RAGEvaluator:
    """
    Automated Evaluation Suite for RAG systems using Ragas.
    Implements core metrics: Faithfulness, Answer Relevancy, Context Precision, and Context Recall.
    """

    def __init__(self, metrics: Optional[List[Any]] = None):
        """
        Initializes the evaluator with a list of metrics.
        
        Args:
            metrics (Optional[List[Any]]): A list of Ragas metrics to use. Defaults to core RAG metrics.
        """
        self.metrics = metrics or [
            faithfulness,
            answer_relevancy,
            context_precision,
            context_recall
        ]

    @time_execution
    def evaluate_rag(self, queries: List[str], answers: List[str], contexts: List[List[str]], ground_truths: Optional[List[str]] = None) -> Dict[str, float]:
        """
        Evaluates the RAG performance on a given dataset.

        Args:
            queries (List[str]): List of user queries.
            answers (List[str]): List of answers generated by the RAG system.
            contexts (List[List[str]]): List of context chunks retrieved for each query.
            ground_truths (Optional[List[str]]): List of gold-standard ground truth answers.

        Returns:
            Dict[str, float]: Dictionary of metric scores.
        """
        data = {
            "question": queries,
            "answer": answers,
            "contexts": contexts
        }
        
        if ground_truths:
            data["ground_truth"] = ground_truths

        dataset = Dataset.from_dict(data)
        
        try:
            result = evaluate(
                dataset=dataset,
                metrics=self.metrics
            )
            return result.to_pandas().mean().to_dict()
        except Exception as e:
            # TODO: Add structured logging
            print(f"Error during RAG evaluation: {str(e)}")
            raise

    def generate_report(self, evaluation_results: Dict[str, float]) -> str:
        """
        Generates a formatted report of the evaluation results.

        Args:
            evaluation_results (Dict[str, float]): The results from evaluate_rag.

        Returns:
            str: A formatted string report.
        """
        report = "RAG System Evaluation Report\n"
        report += "==========================\n"
        for metric, score in evaluation_results.items():
            report += f"{metric.replace('_', ' ').title()}: {score:.4f}\n"
        return report

